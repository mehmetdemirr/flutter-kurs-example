void main() {
  //1'den 10'a Kadar Sayıları Okunuşlarını Yazdırma

  //1 => bir
  //10 => on

  for (int i = 1; i < 11; i++) {
    if (i == 1) {
      print("bir");
    } else if (i == 2) {
      print("iki");
    } else if (i == 3) {
      print("üç");
    } else if (i == 4) {
      print("dört");
    } else if (i == 5) {
      print("beş");
    } else if (i == 6) {
      print("altı");
    } else if (i == 7) {
      print("yedi");
    } else if (i == 8) {
      print("sekiz");
    } else if (i == 9) {
      print("dokuz");
    } else if (i == 10) {
      print("on");
    } else {
      print("belirlenemeyen sayı");
    }
  }
}
